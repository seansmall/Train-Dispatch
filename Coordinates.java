
public class Coordinates {

    private double x,y;
    public Coordinates (double a, double b) {
        x = a;
        y = b;
    }
   public double getX() {
       return x;
   }
   
   public double getY() {
       return y;
   }
   
   public void setX(double a) {
       x = a;
   }
   
   public void setY(double a) {
       y = a;
   }
}
